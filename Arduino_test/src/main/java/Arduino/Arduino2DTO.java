package Arduino;

public class Arduino2DTO {
 private int numb;
 private String usma, pin, name, control;
public int getNumb() {
	return numb;
}
public void setNumb(int numb) {
	this.numb = numb;
}
public String getUsma() {
	return usma;
}
public void setUsma(String usma) {
	this.usma = usma;
}
public String getPin() {
	return pin;
}
public void setPin(String pin) {
	this.pin = pin;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getControl() {
	return control;
}
public void setControl(String control) {
	this.control = control;
}
 
 
}
